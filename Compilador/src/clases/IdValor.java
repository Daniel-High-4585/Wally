package clases;

/**
 *
 * @author cbreaker95
 */
public class IdValor {
    static String id;
    static int valor;
    static String estado;

    public IdValor(String id, int valor, String estado) {
        this.id = id;
        this.valor = valor;
        this.estado = estado;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public int getValor() {
        return valor;
    }

    public void setValor(int valor) {
        this.valor = valor;
    }

    public String getEstado() {
        return estado;
    }

    public void setEstado(String estado) {
        this.estado = estado;
    }
    
}
